#include "../../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	x;
	char			*base;

	base = "0123456789";
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		x = -n;
		write(fd, "-", 1);
	}
	else
		x = n;
	if (x > 9)
	{
		ft_putnbr_fd(x / 10, fd);
		ft_putnbr_fd(x % 10, fd);
	}
	else
		write(fd, &base[x], 1);
}
